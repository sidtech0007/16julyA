package logical_programs;

public class Example3_Even_or_Odd_number 
{
	public static void main(String[] args) 
	{
		int a=848748758;
		
		if (a%2==0) 
		{
			System.out.println("Given number is Even");
			
		}
		else
		{
			System.out.println("Given number is Odd");
		}
		
	}

}

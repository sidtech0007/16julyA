package logical_programs;

public class example2_multiply_2_num_without_multiplicationOperator 
{
	public static void main(String[] args) 
	{
		int num1=10;
		int num2=5;
		
		int sum=0;
		
		for(int i=0; i<=num2; i++)
		{
			sum=sum+num2;
		}
		
		System.out.println("multiplication of "+num1+" & "+num2+ " : "+sum);
		
	}

}
